// Language management functions
let currentLanguage = 'es';

// Language configuration
const languages = {
    es: { flag: '🇪🇸', name: 'Español' },
    en: { flag: '🇺🇸', name: 'English' }
    // To add a new language, add it here:
    // fr: { flag: '🇫🇷', name: 'Français' },
    // pt: { flag: '🇧🇷', name: 'Português' },
    // de: { flag: '🇩🇪', name: 'Deutsch' }
};

function setLanguage(lang) {
    if (!languages[lang]) return;
    
    currentLanguage = lang;
    updatePageContent();
    updateLanguageUI();
    localStorage.setItem('preferred-language', lang);
}

function getText(key, params = {}) {
    let text = translations[currentLanguage][key] || key;
    
    // Replace parameters
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });
    
    return text;
}

function updatePageContent() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const params = JSON.parse(element.getAttribute('data-translate-params') || '{}');
        element.textContent = getText(key, params);
    });
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const params = JSON.parse(element.getAttribute('data-translate-params') || '{}');
        element.placeholder = getText(key, params);
    });
}

function updateLanguageUI() {
    const flagIcon = document.getElementById('flag-icon');
    if (flagIcon) {
        flagIcon.textContent = languages[currentLanguage].flag;
    }
}

function toggleLanguage() {
    const nextLanguage = currentLanguage === 'es' ? 'en' : 'es';
    setLanguage(nextLanguage);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Load saved language preference
    const savedLanguage = localStorage.getItem('preferred-language');
    if (savedLanguage && languages[savedLanguage]) {
        currentLanguage = savedLanguage;
    }
    
    // Set up language toggle
    const languageToggle = document.getElementById('language-toggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', toggleLanguage);
    }
    
    updatePageContent();
    updateLanguageUI();
}); 